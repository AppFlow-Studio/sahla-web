// Point-in-polygon test using ray casting
export function isPointInPolygon(lat: number, lng: number, polygon: [number, number][]): boolean {
  let inside = false;
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const [yi, xi] = polygon[i];
    const [yj, xj] = polygon[j];
    if ((yi > lat) !== (yj > lat) && lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi) {
      inside = !inside;
    }
  }
  return inside;
}

export function isLand(lat: number, lng: number): boolean {
  for (const continent of continents) {
    if (isPointInPolygon(lat, lng, continent)) return true;
  }
  return false;
}

// Generate a canvas texture with shaded continents
export function generateEarthTexture(
  width = 1024,
  height = 512,
  oceanColor = "#071a14",
  landColor = "#153d2e",
): HTMLCanvasElement {
  const canvas = document.createElement("canvas");
  canvas.width = width;
  canvas.height = height;
  const ctx = canvas.getContext("2d")!;

  // Fill ocean
  ctx.fillStyle = oceanColor;
  ctx.fillRect(0, 0, width, height);

  // Draw each continent
  for (const polygon of continents) {
    ctx.beginPath();
    for (let i = 0; i < polygon.length; i++) {
      const [lat, lng] = polygon[i];
      // Equirectangular projection
      const x = ((lng + 180) / 360) * width;
      const y = ((90 - lat) / 180) * height;
      if (i === 0) ctx.moveTo(x, y);
      else ctx.lineTo(x, y);
    }
    ctx.closePath();
    ctx.fillStyle = landColor;
    ctx.fill();
  }

  return canvas;
}
